    private void tb_CampMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_CampMouseClicked
         if(clickable == true){
             row = tb_Camp.getSelectedRow();
         boolean fn=false;
         
        if(row!=-1){
            
            txt_CampId.setText(""+tb_Camp.getValueAt(row, 0));
            txt_CampId.setEditable(false);
            
            txt_CampName.setText(""+tb_Camp.getValueAt(row, 1));
            txt_CampName.setEditable(false);
            
            txt_ProID.setText(""+tb_Camp.getValueAt(row, 2));
            txt_ProID.setEditable(false);
           
            String proName = dataPC.getAnotherField("Product", "ProID", "ProName", tb_Camp.getValueAt(row, 2).toString());
            cmb_Pro.setSelectedItem(proName);
            cmb_Pro.setEnabled(false);
            
            txt_Start.setText(""+tb_Camp.getValueAt(row, 3));
            txt_Start.setEditable(false);
            
            txt_End.setText(""+tb_Camp.getValueAt(row, 4));
            txt_End.setEditable(false);
            
            txt_per.setText(""+tb_Camp.getValueAt(row, 5));
            txt_per.setEditable(false);
            
            txt_AdID.setText(""+tb_Camp.getValueAt(row, 6));
            txt_AdID.setEditable(false);
           
            cmb_Ad.setSelectedItem(tb_Camp.getValueAt(row, 7));
            cmb_Ad.setEnabled(false);
            
            txt_Des.setText(""+tb_Camp.getValueAt(row, 8));
            txt_Des.setEditable(false);
           
            if(tb_Camp.getValueAt(row, 9).equals("Yes")) {
                fn=true;
            }
            else{
                fn=false;
            }
            chk_finish.setSelected(fn);
            chk_finish.setEnabled(false);
        }
         }
    }//GEN-LAST:event_tb_CampMouseClicked

    private void btn_createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_createActionPerformed
        int cr_row = 0;
        if(evt.getActionCommand().equals("Create")){
            reset();
            clickable = false;
            btn_create.setText("Save");
            //Deactivate buttons
            btn_del.setEnabled(false);
            btn_Find.setEnabled(false);
            btn_refresh.setEnabled(true);
            btn_update.setEnabled(false);
            //Activate Edit for textbox and combobox and checkbox
            txt_CampName.setEnabled(true);
            
            txt_CampName.setEditable(true);
            txt_Start.setEnabled(true);
            txt_Start.setEditable(true);
            txt_End.setEnabled(true);
            txt_End.setEditable(true);
            txt_Des.setEnabled(true);
            txt_Des.setEditable(true);
            
            cmb_Ad.setEnabled(true);
            cmb_Pro.setEnabled(true);
            chk_finish.setEnabled(false);
            chk_finish.setSelected(false);
                      
        }
        if(evt.getActionCommand().equals("Save")){
            if(Validation.isEmpty(txt_CampName.getText().trim())){
                Validation.showMess("Empty value is not allowed");
                txt_CampName.requestFocus();
                txt_CampName.setBackground(new Color(255,153,153));
                return;
            }else if(((txt_CampName.getText().trim()).length()>50))
        {
            Validation.showMess("Campaign Name is restricted to 50 characters");
            txt_CampName.requestFocus();
            txt_CampName.setBackground(new Color(255,153,153));
            return;
        }
            else if(cmb_Pro.getSelectedIndex()==0){
                Validation.showMess("Must choose a product");
                cmb_Pro.requestFocus();
                return;
            }
            else if(cmb_Ad.getSelectedIndex()==0){
                Validation.showMess("Must choose an Ad-Method");
                cmb_Ad.requestFocus();
                return;
            }
         if(!Validation.validateComponents(new JTextComponent[]{txt_ProID,txt_CampName,txt_Start,txt_End,txt_AdID,}))
        {
            return;
        }          
        else if(!Validation.isDate(txt_Start.getText()))
        {
            Validation.showMess("Date invalid fomat dd/mm/yyyy ");
            txt_Start.requestFocus();
            txt_Start.setBackground(new Color(255,153,153));
            return;
        }
        else if(!Validation.isDate(txt_End.getText()))
        {
            Validation.showMess("Date invalid fomat dd/mm/yyyy ");
            txt_End.requestFocus();
            txt_End.setBackground(new Color(255,153,153));
            return;
        }
       
        else if(Validation.convertStringToDate(txt_Start.getText()).after(Validation.convertStringToDate(txt_End.getText())))
        {
            Validation.showMess("Finish date must be after start date  ");
            txt_End.setBackground(new Color(255,153,153));
            txt_End.requestFocus();
            return;            
        }else if(((txt_Des.getText().trim()).length()>150))
        {
            Validation.showMess("Campaign's Description is restricted to 150 characters");
            txt_Des.requestFocus();
            txt_Des.setBackground(new Color(255,153,153));
            return;
        }          
        String mess=CampaignPC.insertCampaign(getCampaign());
        new ShowMessageDialog(null, true, mess, ShowMessageDialog.MESSAGE);
        reset();
        btn_create.setText("Create");
        clickable = true;
        //Deactivate buttons
        reset();
        }
    }//GEN-LAST:event_btn_createActionPerformed

