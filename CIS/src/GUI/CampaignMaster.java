    private Campaign getCampaign()
    {
        Campaign cam=new Campaign();
        Date start = Validation.convertStringToDate(txt_Start.getText().trim());
        Date end = Validation.convertStringToDate(txt_End.getText().trim());
        
        if(update == true){
            cam.setCampaignID(Integer.parseInt(txt_CampId.getText().trim()));
        }
        cam.setCampaignName(txt_CampName.getText().trim());
        cam.setProductID(txt_ProID.getText().toString().trim());
        cam.setProductName(cmb_Pro.getSelectedItem().toString().trim());
        cam.setStartDate(Validation.convertStringToDate(txt_Start.getText().trim()));
        cam.setEndDate(Validation.convertStringToDate(txt_End.getText().trim()));
        cam.setPeriod(start.compareTo(end));
        cam.setAdID(txt_AdID.getText());
        cam.setAdType(cmb_Ad.getSelectedItem().toString().trim());
        cam.setDescription(txt_Des.getText().toString().trim());
        cam.setEnded(chk_finish.isSelected());

        return cam;
    }
    
