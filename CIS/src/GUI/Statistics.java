    private void fillTable()
    {
        Vector title = new Vector();
        title.add("Campaign ID");
        title.add("Campaign Name");
        title.add("Estimated Variance");
        title.add("Actual Variance");
        title.add("Final Variance");


        Vector campaigns = StatisticPC.readCampaign();
        if(campaigns.size()>0)
        {
            tb_camp.setModel(new DefaultTableModel(campaigns, title));
            tb_camp.getColumnModel().getColumn(0).setPreferredWidth(60);
            tb_camp.getColumnModel().getColumn(1).setPreferredWidth(90);
            tb_camp.getColumnModel().getColumn(2).setPreferredWidth(70);
            tb_camp.getColumnModel().getColumn(3).setPreferredWidth(70);
            tb_camp.getColumnModel().getColumn(4).setPreferredWidth(70);
            tb_camp.setDragEnabled(false);
        }

    }
    
