    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        int ByID=1;
        int ByName=2;
        Vector vt;
        if(Validation.isEmpty(txt_search.getText()))
        {
            Validation.showMess("Please enter any words to find");
            txt_search.requestFocus();
            txt_search.setBackground(new Color(255,153,153));
            return;
        }
        if(rd_Id.isSelected())
        {
            vt=StatisticPC.FindCampaign(txt_search.getText(), ByID);
        }
        else
        {
            vt=StatisticPC.FindCampaign(txt_search.getText(), ByName);
        }
        Vector tit=new Vector();
        tit.add("Campaign ID");
        tit.add("Campaign Name");
        tit.add("Estimated Variance");
        tit.add("Actual Variance");
        tit.add("Final Variance");
        tb_camp.setModel(new DefaultTableModel(vt, tit));
        txt_search.setText("");
    }//GEN-LAST:event_btn_searchActionPerformed

